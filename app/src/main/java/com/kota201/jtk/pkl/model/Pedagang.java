public class Pedagang {
    private String idPedagang;
    private String noPonselPedagang;
    private String passwordPedagang;
    private String namaPedagang;
    private String emailPedagang;
    private String alamatPedagang;
    private String fotoPedagang;

    public String getIdPedagang() {
        return idPedagang;
    }

    public void setIdPedagang(String idPedagang) {
        this.idPedagang = idPedagang;
    }

    public String getNoPonselPedagang() {
        return noPonselPedagang;
    }

    public void setNoPonselPedagang(String noPonselPedagang) {
        this.noPonselPedagang = noPonselPedagang;
    }

    public String getPasswordPedagang() {
        return passwordPedagang;
    }

    public void setPasswordPedagang(String passwordPedagang) {
        this.passwordPedagang = passwordPedagang;
    }

    public String getNamaPedagang() {
        return namaPedagang;
    }

    public void setNamaPedagang(String namaPedagang) {
        this.namaPedagang = namaPedagang;
    }

    public String getEmailPedagang() {
        return emailPedagang;
    }

    public void setEmailPedagang(String emailPedagang) {
        this.emailPedagang = emailPedagang;
    }

    public String getAlamatPedagang() {
        return alamatPedagang;
    }

    public void setAlamatPedagang(String alamatPedagang) {
        this.alamatPedagang = alamatPedagang;
    }

    public String getFotoPedagang() {
        return fotoPedagang;
    }

    public void setFotoPedagang(String fotoPedagang) {
        this.fotoPedagang = fotoPedagang;
    }
}
