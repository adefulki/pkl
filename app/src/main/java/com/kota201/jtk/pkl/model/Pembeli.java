public class Pembeli {
    private String idPembeli;
    private String noPonselPembeli;
    private String passwordPembeli;
    private String namaPembeli;
    private String emailPembeli;
    private String alamatPembeli;
    private String fotoPembeli;

    public String getIdPembeli() {
        return idPembeli;
    }

    public void setIdPembeli(String idPembeli) {
        this.idPembeli = idPembeli;
    }

    public String getNoPonselPembeli() {
        return noPonselPembeli;
    }

    public void setNoPonselPembeli(String noPonselPembeli) {
        this.noPonselPembeli = noPonselPembeli;
    }

    public String getPasswordPembeli() {
        return passwordPembeli;
    }

    public void setPasswordPembeli(String passwordPembeli) {
        this.passwordPembeli = passwordPembeli;
    }

    public String getNamaPembeli() {
        return namaPembeli;
    }

    public void setNamaPembeli(String namaPembeli) {
        this.namaPembeli = namaPembeli;
    }

    public String getEmailPembeli() {
        return emailPembeli;
    }

    public void setEmailPembeli(String emailPembeli) {
        this.emailPembeli = emailPembeli;
    }

    public String getAlamatPembeli() {
        return alamatPembeli;
    }

    public void setAlamatPembeli(String alamatPembeli) {
        this.alamatPembeli = alamatPembeli;
    }

    public String getFotoPembeli() {
        return fotoPembeli;
    }

    public void setFotoPembeli(String fotoPembeli) {
        this.fotoPembeli = fotoPembeli;
    }
}
